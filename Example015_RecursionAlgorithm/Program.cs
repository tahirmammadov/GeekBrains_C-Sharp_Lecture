void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write(" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col, int[,] array)
{
    if (array[row, col] == 0)
    {
        array[row, col] = 1;
        FillImage(row - 1, col, array);
        FillImage(row, col - 1, array);
        FillImage(row + 1, col, array);
        FillImage(row, col + 1, array);

    }
}

